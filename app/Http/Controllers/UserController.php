    {
        $validator = Validator::make($request->all(), [
            'name' => 'required | min:5 ',
            'country_id' => 'required',
            'date_of_birth' => ['required', 'before:18 years ago'] 
        ]);

        if($validator->fails()){
            return Redirect::back()->withErrors($validator);
        }
        // Validate input
        
        // Create new product
        User::create($request->all());
        //
        return redirect()->back();
    }

